def square(number):
    """
    :param number: Entered number to multiply by itself
    :return: Mathmatically squared input.
    """
    return number ** 2


def double(number):
    """
    :param number: Entered number to be multiplied by 2
    :return: Mathmatically doubled input.
    """
    return number * 2


def add(num1, num2):
    """
    :param num1: First number to sum
    :param num2: Second number to sum
    :return: Sum of input parameters
    """
    return num1 + num2